// definimos tipos de datos

let numero = 4; //numeros
let texto = "Hola Mundo";//nombres o texto
let booleano = true;// verdadera = true o false = falso
let indefinido; // no tiene valor definido
let nulo = null; // valor nulo o vacio
let objeto = {nombre: "jack", edad: 8} ;// objeto o prioridades

// mostramos los valores

console.log("Número:", numero); //Esto es un número
console.log("Texto:", texto); //Esto es un texto
console.log("Booleano:", booleano); //Esto es un booleano (v o f)
console.log("Indefinido:", indefinido); //Esto es indefinido (la variable no tiene valor)
console.log("Nulo:", nulo); //Esto es nulo (representa un valor vacío)
console.log("Objeto:", objeto); //Esto es un objeto (con nombre y edad)